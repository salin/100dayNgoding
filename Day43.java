package ngoding;

public class Day43 {
    public static void main(String[] args) {
        int angka = 3 ;
        for (int i = 1; i <= angka ; i++) {
            for (int j = 1; j < angka; j++) {
                System.out.print("* ");
            }
            System.out.println("* ");
            
        }
        System.out.println("");
    }
    
}
