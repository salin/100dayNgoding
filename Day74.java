package ngoding;
public class Day74 {
    public static void main(String[] args) {
        int[][] array = {
            {5,23},
            {38,44},
            {56,69}
        };
        for (int i = 0; i < array.length; i++) {
            for (int j = 0; j < array[i].length; j++) {
                System.out.print(array[i][j]);
            }System.out.println();
        }
    
    }
    
}