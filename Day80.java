package ngoding;

public class Day80 {

    public static void main(String[] args) {

        double angka = -12.3;

        if (angka < 0.0) {
            System.out.println(angka + "angka negatif");
        } else if (angka > 0.0) {
            System.out.println(angka + "angka positif");
        } else {
            System.out.println(angka + "angka 0  ");
        }
    }
}
